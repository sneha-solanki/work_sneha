  <tr>
    <td class="text">At Seven Source, we follow a carefully structured selection process, which start right from understanding the clients' needs and functions all the way to follow-ups with both clients and candidates post recruitment. A brief description of the different stages involved in our recruitment process is given below: - </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2"> Planning </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">In the planning stage, we evaluate the needs and requirements of our clients to determine the required set of skills, understand organizational behavior and relationships. Working as a cohesive team with our client, we identify the required experience and other characteristics, which are necessary for the successful candidate. </td>
    <td>&nbsp;</td>
  </tr>
  
  <tr>
    <td colspan="2">We select those candidates, who fit in well with the company's plan. In order to find the suitable candidate we give prime consideration to the company's existing business activities, business plans, work-culture, structures, systems and future plans. We concentrate on the job description, its key result areas, opportunities, career implications, location, salary and benefits.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">The initial planning enable us in identifying the needs of both the company as well as the candidates, which further leads us in deciding on a search strategy to target on specific skills, education, personality and experience.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Mapping Exercise </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">During the mapping exercise, we performing industry specific desk and field research on companies and relevant people so as to identify the key competencies. It includes job title, principal accountabilities, key result areas (KRAs), authority to be infested, external / internal contacts, reporting procedures and supervisory relationships, working conditions / environment, job specifications, compensation package, location and other necessary details. </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" rowspan="2">Long List Generation &nbsp;The Field research provides us a long list of qualified candidates, who are worth pursuing. After this process, we start the search to identify the suitable candidates either from our own exhaustive database or by advertising for the position. </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Implementation</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">The implementation stage consists of several phases including, preliminary interviews, short-listing of candidates, informal reference checks, client interviews and formal reference checks. </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Preliminary Interviews</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">For preliminary interviews, we select the potential candidates on the basis of prior work experience, qualifications, strength and weaknesses and the ability of candidate to fit into the work-culture and ethics of the client organization. The potential candidates are contacted and interviews will be conducted to find out their interest and suitability.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">We deploy the necessary expert assessment after the screening of candidates by drawing up competency profiles through the most innovative proprietary platform. During this phase, our consultants keep on updating the client of the progress achieved.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Short List Generation</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">The preliminary interviews result out in short-listing of highly qualified candidates. Confidential reports are generally prepared and submitted in writing to our clients on four or five individuals, who fulfill the specified position closely. &nbsp;</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Informal reference Checks</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">We conduct informal reference check to verify the listed achievements and last few work experiences of the short-listed candidates. After this phase, a list is handed to the client with our recommendations and assessments.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Client Interviews</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">After the informal reference checks, we schedule a meeting between the short-listed candidates and the client with proper travel arrangements and handling of reimbursement of travel expenses. The consultant asks both the client and the candidates for the feedback after every interview.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Formal Reference Checks</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">After client interviews, we conduct a formal reference check of the selected candidates. Our team contacts individuals / companies that are capable of providing details of the candidate's qualifications, work ethics and other relevant feed back. The comments made by these references are then reviewed with the client.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Closure</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">The closure stage consists of negotiation & offer, Follow-up / progress and handholding. </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Negotiation & Offer</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">We actively participate in the negotiation process including compensation. Both the client and the candidates draw on our in-depth knowledge of market compensation and benefits in order to discuss and plan a competitive and equitable package.</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Follow Up / Progress</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">Understanding the importance of follow up / progress phase, we keep in touch with the candidate until the time he / she joins the company. </td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2" class="point2">Handholding</td>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">After the successful candidate joining for a specified job, we keep an ongoing dialogue with both the candidate and the client. This handholding process ensures a smooth integration into the organization and accomplishment of targets and performance goals.</td>
